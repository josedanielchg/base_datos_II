def agregar_orden_id(consulta_sql):
    # Dividir la consulta original en registros individuales
    partes = consulta_sql.strip().rstrip(';').split("),")
    # Preparar la lista para guardar los registros actualizados
    registros_actualizados = []
    # Agregar OrdenID a cada registro
    for i, registro in enumerate(partes):
        if i < len(partes) - 1:
            registro += ')'  # Añadir el paréntesis cerrado que fue removido en el split
        # Añadir OrdenID después de 'VALUES ('
        posicion_values = registro.find("VALUES (") + 8
        # Componer el nuevo registro con el OrdenID añadido
        registro_actualizado = registro[:posicion_values] + str(i + 1) + ", " + registro[posicion_values:]
        registros_actualizados.append(registro_actualizado)
    # Reconstruir la consulta con los registros actualizados
    consulta_actualizada = ",\n".join(registros_actualizados) + ";"
    return consulta_actualizada


